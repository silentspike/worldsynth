    phase = 0.0;
    var timer_h = std.time.Timer.start() catch unreachable;
    for (0..iterations) |_| {
        wt.process_block(&out, &phase, phase_inc, 0.0);
        std.mem.doNotOptimizeAway(&out);
    }
    const ns_hermite = timer_h.read() / iterations;

    // Measure Linear
    phase = 0.0;
    var timer_l = std.time.Timer.start() catch unreachable;
    for (0..iterations) |_| {
        wt.process_block_linear(&out, &phase, phase_inc, 0.0);
        std.mem.doNotOptimizeAway(&out);
    }
    const ns_linear = timer_l.read() / iterations;

    const ratio: f64 = if (ns_linear > 0) @as(f64, @floatFromInt(ns_hermite)) / @as(f64, @floatFromInt(ns_linear)) else 0.0;
    // In debug mode, inline hints are ignored, so Hermite overhead is much higher.
    // Only enforce the ratio in ReleaseFast.
    // Hermite: 4 table reads + cubic polynomial (~10 FMA ops)
    // Linear: 2 table reads + 1 lerp — ratio ~2-3x is expected.
    const max_ratio: f64 = if (strict) 2.5 else 10.0;
    const pass = ratio < max_ratio;
    std.debug.print("\n[WP-050] Hermite: {}ns, Linear: {}ns, ratio: {d:.2}x (max: {d:.1}x) {s}\n", .{ ns_hermite, ns_linear, ratio, max_ratio, if (pass) "PASS" else "<<< FAIL >>>" });
    try std.testing.expect(pass);
}
