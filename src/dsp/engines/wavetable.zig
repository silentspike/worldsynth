test "AC-1: Sine wavetable at 440Hz — error < 1e-3 vs sin()" {
    const wt = try std.testing.allocator.create(WavetableEngine);
    defer std.testing.allocator.destroy(wt);
    wt.init();

    var sine_frame: [FRAME_SIZE]f32 = undefined;
    generate_sine_frame(&sine_frame);
    wt.load_frame(0, &sine_frame);
    wt.generate_mip_maps();

    const sample_rate: f64 = 44100.0;
    const freq: f64 = 440.0;
    const phase_inc: f64 = freq / sample_rate;
    var phase: f64 = 0.0;

    var out: [BLOCK_SIZE]f32 = undefined;
    wt.process_block(&out, &phase, phase_inc, 0.0);

    // Compare against reference sin()
    var ref_phase: f64 = 0.0;
    var max_err: f32 = 0.0;
    for (out) |s| {
        const ref: f32 = @floatCast(@sin(2.0 * std.math.pi * ref_phase));
        const err = @abs(s - ref);
        max_err = @max(max_err, err);
        ref_phase += phase_inc;
    }
    std.debug.print("\n[WP-050] Sine 440Hz max error: {d:.6}\n", .{max_err});
    try std.testing.expect(max_err < 1e-3);
}

test "AC-2: Frame position 0.5 interpolates between frames" {
    const wt = try std.testing.allocator.create(WavetableEngine);
    defer std.testing.allocator.destroy(wt);
    wt.init();

    var sine_frame: [FRAME_SIZE]f32 = undefined;
    var saw_frame: [FRAME_SIZE]f32 = undefined;
    generate_sine_frame(&sine_frame);
    generate_saw_frame(&saw_frame);
    wt.load_frame(0, &sine_frame);
    wt.load_frame(1, &saw_frame);
    wt.generate_mip_maps();

    const phase_inc: f64 = 440.0 / 44100.0;

    // Process at frame_pos = 0.0 (pure sine)
    var phase0: f64 = 0.0;
    var out0: [BLOCK_SIZE]f32 = undefined;
    wt.process_block(&out0, &phase0, phase_inc, 0.0);

    // Process at frame_pos = 1.0 (pure saw)
    var phase1: f64 = 0.0;
    var out1: [BLOCK_SIZE]f32 = undefined;
    wt.process_block(&out1, &phase1, phase_inc, 1.0);

    // Process at frame_pos = 0.5 (blend)
    var phase_mid: f64 = 0.0;
    var out_mid: [BLOCK_SIZE]f32 = undefined;
    wt.process_block(&out_mid, &phase_mid, phase_inc, 0.5);

    // The blend output must differ from both pure sine and pure saw
    var diff_sine: f32 = 0.0;
    var diff_saw: f32 = 0.0;
    for (out_mid, out0, out1) |mid, s, w| {
        diff_sine += @abs(mid - s);
        diff_saw += @abs(mid - w);
    }
    std.debug.print("\n[WP-050] Frame blend diff vs sine: {d:.4}, vs saw: {d:.4}\n", .{ diff_sine, diff_saw });
    try std.testing.expect(diff_sine > 0.01);
    try std.testing.expect(diff_saw > 0.01);
