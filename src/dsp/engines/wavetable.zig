test "FFT/IFFT roundtrip preserves signal" {
    var re: [FRAME_SIZE]f32 = undefined;
    var im = [_]f32{0.0} ** FRAME_SIZE;
    // Fill with a known signal (sine at bin 10)
    for (&re, 0..) |*s, i| {
        s.* = @sin(2.0 * std.math.pi * 10.0 * @as(f32, @floatFromInt(i)) / @as(f32, @floatFromInt(FRAME_SIZE)));
    }
    var original: [FRAME_SIZE]f32 = undefined;
    @memcpy(&original, &re);

    fft(&re, &im, false); // Forward FFT
    fft(&re, &im, true); // Inverse FFT

    var max_err: f32 = 0.0;
    for (re, original) |r, o| {
        max_err = @max(max_err, @abs(r - o));
    }
    std.debug.print("\n[WP-050] FFT roundtrip max error: {d:.8}\n", .{max_err});
    try std.testing.expect(max_err < 1e-4);
}

