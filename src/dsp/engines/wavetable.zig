test "mip-level selection correctness" {
    // Very low frequency → level 0 (product = 2048 * 20/44100 ≈ 0.93 < 1)
    try std.testing.expectEqual(@as(usize, 0), select_mip_level(20.0 / 44100.0));
    // 440Hz → level 5 (product = 2048 * 440/44100 ≈ 20.4, ceil(log2(20.4)) = 5)
    try std.testing.expectEqual(@as(usize, 5), select_mip_level(440.0 / 44100.0));
    // Very high frequency → high mip level
    const high_level = select_mip_level(10000.0 / 44100.0);
    try std.testing.expect(high_level > 5);
    // Negative/zero → level 0
    try std.testing.expectEqual(@as(usize, 0), select_mip_level(0.0));
    try std.testing.expectEqual(@as(usize, 0), select_mip_level(-1.0));
}

